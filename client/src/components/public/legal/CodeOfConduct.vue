  <h2>Code of Conduct</h2>
  <p>
    Our conduct is separated from our game rules, and covers how we expect people attending our events to behave.
  </p>

  <p>
    As part of attending the event, we expect every player to be familiar with our code of conduct. It is essential 
    that we cultivate a community wherein everyone is on the same page, and can voice any concerns that they may. 
    Below is a list of things that are never appropriate when attending our events:
  </p>

  <p>
    It is not acceptable to insult another player on the basis of their physical appearance and attributes. This 
    includes, but is by no means limited to: height, weight, skin colour, or any other out-of-character aspect of 
    their appearance. Discriminatory behaviour pertaining to such is intolerable.
  </p>

  <p>
    Sexual harassment and Objectification. This includes inappropriate remarks aimed toward another person, in which 
    they may be made to feel uncomfortable or uneasy, as well as unwelcome physical contact.
  </p>

  <p>
    Discriminatory behaviour towards any individual on the basis of any disability they may have. We expect all of our 
    attendants to be mindful of disabilities, visible or otherwise, and will not tolerate discriminatory behaviour 
    concerning such.
  </p>

  <p>
    Transphobia &amp; Homophobia. Prejudice regarding any player&#39;s gender expression, identity, sex or sexual
    orientation will not be tolerated. This includes consistent and deliberate misuse of pronouns, or derogatory 
    comments towards a person&rsquo;s sexual preferences.
  </p>

  <p>
    Sexism of any fashion will not be tolerated at our games. This includes any and all derogatory comments that could 
    be considered to be misogynistic, misandric, or exclusionary toward any sex.
  </p>

  <p>
    Racism will not be abided by. Espousing any form of racist rhetoric, or comment, pertaining to another person&rsquo;s 
    cultural identity or ethnicity is intolerable. This extends to generalisations aimed towards minority groups, as 
    well as targeted attacks on individuals.
  </p>

  <p>
    Discrimination based on a player&rsquo;s out-of-character age. Patronising or condescending a player due to their 
    age is completely unacceptable. Whilst it exists throughout the game regarding the age of certain expressions in 
    comparison to another, it should never be grounded on the real-world age of a player at any given time.
  </p>

  <p>
    We expect all players to remain respectful and kind toward members of staff at any venue we attend. This also 
    extends to the organisers and members of the crew present during our games.
  </p>
  <p>
    Any incidents of direct physical assault, or sexual harassment, should immediately be brought up with game 
    organisers at our event (we will make ourselves known to you prior to time in).
  </p>
  <p>
    Excessive inebriation of any kind will not be tolerated at any of our events. If you are deemed to be too 
    intoxicated, you will likely be removed from the game.
  </p>

  <p>
    Classist remarks pertaining to the out-of-character wealth and/or background of any player will not be tolerated. 
    This is fundamentally distinct from commenting on a character&rsquo;s faction, resources, or background: the ability
    to do such being an integral part of the setting.
  </p>

  <p>
    Out of character bullying will not be tolerated. This also includes continued social PvP wherein one party has 
    requested that, for whatever reason, it stops.
  </p>

  <p>
    We do not permit discussions pertaining to sexual assault at all, at our games. This also applies to references 
    to miscarriages, infanticide, and all forms of non-consenual sexual activity.
  </p>

  <p>
    We expect our players to always be aware that anyone they may be interacting with may be uncomfortable with 
    triggering topics or sensitive issues that are prevalent in the setting. Breaking character to talk about 
    boundaries, social, physical, or otherwise, is not immersion breaking. It is encouraged that if you are ever 
    unsure with whether people are uncomfortable with a topic, or you are unsure of any potential physical boundary, 
    that you drop out of character and discuss this with whoever is involved in the scene.
  </p>

  <p>The event is 13+. Anyone found to be underage and attending, will be asked to leave.</p>
  
  <p>
    We encourage players to come to us directly (through our listed means of contact, as found on the website, or 
    in person at events) if they have any concerns regarding the conduct of a person, or group.
  </p>
